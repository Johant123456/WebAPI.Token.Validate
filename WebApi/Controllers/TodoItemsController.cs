            var securityKey = new SymmetricSecurityKey(Encoding.UTF8.GetBytes(key));
            var credentials = new SigningCredentials(securityKey, SecurityAlgorithms.HmacSha256);


            string[] groups = new string[] { "1", "q" };
            //Create a List of Claims, Keep claims name short    
            var permClaims = new List<Claim>();
            permClaims.Add(new Claim(JwtRegisteredClaimNames.Jti, Guid.NewGuid().ToString()));
            permClaims.Add(new Claim("valid", "1"));
            permClaims.Add(new Claim("userid", "APIuser"));
            permClaims.Add(new Claim("name", "John Rad"));
            permClaims.Add(new Claim("groups", "YG-Slack"));
            permClaims.Add(new Claim("groups", "PG8-TFS-All-Project-Admins"));
            permClaims.Add(new Claim("groups", "RG-JIRA-GENERAL-USR"));
            permClaims.Add(new Claim("groups", "YG-WCDTR-USERS"));
            permClaims.Add(new Claim("groups", "SG-INS-ms-skypeforbusiness-en-pilot"));
            permClaims.Add(new Claim("groups", "QG-PrintRelease_07_Backup"));


            //Create Security Token object by giving required parameters    
            var token = new JwtSecurityToken(issuer, //Issure    
